int		ft_atoi(const char *str)
{
	int		n;
	int		sign;

	n = 0;
	sign = 1;
	while (*str)
	{
		while (*str == 32 || (*str >= 9 && *str <= 13))
			++str;
		if (*str == '+' || *str == '-')
			sign = (*str++ == '-') ? -1 : 1;
		while (*str >= '0' && *str <= '9')
			n = n * 10 + (*str++ - '0');
	}
	return (n * sign);
}
